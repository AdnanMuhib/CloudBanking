    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newCheque = new Cheque;
        $email = Auth::user()->email;
        $data = User::where('email','=',$email)->get();
        $newCheque->customer_id = $data[0]->id;
        $newCheque->amount = $request->amount;
        $newCheque->amount_in_words = $this->convert($request->amount);
        $newCheque->payee_name = $request->payee_name;
        $newCheque->ip_address = $_SERVER['REMOTE_ADDR'];
        $newCheque->mac_address = $this->get_client_mac();
        $newCheque->save();

        return redirect('/cheque')->with('message', 'Created Successfully');
    }

